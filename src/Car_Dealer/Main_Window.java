    private void KeyRelesed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_KeyRelesed
        // TODO add your handling code here:
        //Detect and destroy non number values
        if (! Character.isDigit(evt.getKeyChar()) &  evt.getKeyCode()!=KeyEvent.VK_BACKSPACE) {
            evt.consume();
            //If consume does not work, then
            String st;  //Local Variable
            try{
                if (evt.getComponent().getName() == jTxtBasicPrice.getName()) {
                st = jTxtBasicPrice.getText();
                jTxtBasicPrice.setText(st.substring(0,(st.length()- 1)));
               
            }else if (evt.getComponent().getName() == jTxtAllowance.getName()) {
                st = jTxtAllowance.getText();
                jTxtAllowance.setText(st.substring(0,(st.length()- 1)));
               
            }else if (evt.getComponent().getName() == jTxtPhone.getName()) {
                st = jTxtPhone.getText();
                jTxtPhone.setText(st.substring(0,(st.length()- 1)));
            }
            }catch (Exception ex ){
            }
        }
    }//GEN-LAST:event_KeyRelesed

