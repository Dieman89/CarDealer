    private void jBtnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCalculateActionPerformed
        // TODO add your handling code here:
        //Getting user inputs
        String st =jTxtBasicPrice.getText();
        String st2 =jTxtAllowance.getText();
                
        try{
            
            //Develop programs using simple and complex conditions to a given specification.
            
                //Check if the user left the value field empty
            if (! st.isEmpty() & ! st2.isEmpty() & ! jTxtNames.getText().isEmpty() & ! jTxtPhone.getText().isEmpty()
                    & ! jTxtPostCode.getText().isEmpty() & ! jTxtAddress.getText().isEmpty()) {
                //Converting to double and assigning values
                SubTotal = Double.parseDouble(st);
                Trade_In_Allowance = Double.parseDouble(st2);
                
                //Checking extra selected features
                if (jCkBoxStereo.isSelected()) {
                    SubTotal += Stereo_System;
                }
                if (jCkBoxDetailing.isSelected()) {
                    SubTotal += Customized_Detailing;
                }
                if (jCkBoxLeather.isSelected()) {
                    SubTotal += Leather_Interior;
                }
                if (jCkBoxGPS.isSelected()) {
                    SubTotal += GPS;
                }
                if (jCkBoxGPS.isSelected()) {
                    SubTotal += Customized_Detailing;
                }
                if (jCkBoxModified.isSelected()) {
                    SubTotal += Modified;
                }
                if (jCkBoxStandard.isSelected()) {
                    SubTotal += Standard;
                }               
                //Set subTotal
                double round = Math.round(SubTotal * 100.0) / 100.0;
                jTxtSubTotal.setText("£" + round);
                        

                //get, set Tax
                //Calling procedures from other procedures and manipulating dates
                Tax= findTax(SubTotal, 0.06);   //Calling procedures.
                
               // st =  String.valueOf(Tax.floatValue());
               round = Math.round(Tax * 100.0) / 100.0;

                jTxtTax.setText("£" + round);
              //  double d1 = Double.parseDouble(jTxtSubTotal.getText());
              //  double roundSub = Math.round(d1 * 100.0) / 100.0;
                
                //get, set Total
                Total = (SubTotal + Tax) - Trade_In_Allowance;
              //  st =  String.valueOf(Math.round(Total * 100.0) / 100.0);
              //  d1 = Double.parseDouble(jTxtAmountDue.getText());
              //  double roundTotal = Math.round(d1 * 100.0) / 100.0;
              
              round = Math.round(Total * 100.0) / 100.0;
                jTxtAmountDue.setText("£" + round);

                jtp.setBackground(new Color(25,30,42));
                jtp.setVisible(true);
                String str;
                str = "Basic Price: \t\t" + "£" + jTxtBasicPrice.getText();
                str += "\n" + "Trade-in-Allowance: \t" +"£" + jTxtAllowance.getText();
                str += "\n" + "Subtotal: \t\t" + jTxtSubTotal.getText();
                str += "\n" + "Tax: \t\t\t" + jTxtTax.getText();
                str += "\n" + "Total: \t\t\t" + jTxtAmountDue.getText();
                str += "\n" + "Name: \t\t\t" + jTxtNames.getText();
                str += "\n" + "Phone: \t\t\t" + jTxtPhone.getText();
                str += "\n" + "Post Code: \t\t" + jTxtPostCode.getText();
                str += "\n" + "Address: \t\t" + jTxtAddress.getText();
                               

                jtp.setText(str); 
            }else      {
            JOptionPane.showMessageDialog(this,  "Empty Value provided");
            }
        }
        catch(Exception ex){
            //Display caught error
            JOptionPane.showMessageDialog(this, "Error performing Operation: " + ex.getMessage());
        }       
    }//GEN-LAST:event_jBtnCalculateActionPerformed
